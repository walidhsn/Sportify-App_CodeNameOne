public class Client_CtegoryForm  extends Form {

    Resources theme = UIManager.initFirstTheme("/theme_1");

    public Client_CtegoryForm(Form previous) {
        super("Category", BoxLayout.y());

        this.add(new InfiniteProgress());
        Display.getInstance().scheduleBackgroundTask(() -> {
            // this will take a while...

            Display.getInstance().callSerially(() -> {
                this.removeAll();

                try {
                    for (category c : new Category_Service().getAllcategorys()) {

                        System.out.println(c);

                        this.add(addIteam_category(c));
                    }
                } catch (Exception ex) {

                }

                this.revalidate();
            });
        });

        this.getToolbar().addSearchCommand(e -> {
            String text = (String) e.getSource();
            if (text == null || text.length() == 0) {
                // clear search
                for (Component cmp : this.getContentPane()) {
                    cmp.setHidden(false);
                    cmp.setVisible(true);
                }
                this.getContentPane().animateLayout(150);
            } else {
                text = text.toLowerCase();
                for (Component cmp : this.getContentPane()) {
                    MultiButton mb = (MultiButton) cmp;
                    String line1 = mb.getTextLine1();
                    String line2 = mb.getTextLine2();
                    boolean show = line1 != null && line1.toLowerCase().indexOf(text) > -1
                            || line2 != null && line2.toLowerCase().indexOf(text) > -1;
                    mb.setHidden(!show);
                    mb.setVisible(show);
                }
                this.getContentPane().animateLayout(150);
            }
        }, 4);
       
       
    }

    public MultiButton addIteam_category(category c) {
        MultiButton m = new MultiButton();
        m.setTextLine1(c.getName());
        m.setEmblem(theme.getImage("round.png"));
        m.addActionListener(l -> {
            Client_EquipmentFOrm w = new Client_EquipmentFOrm(c.getId());
            
            w.show();
        });
        return m;
    }

}
